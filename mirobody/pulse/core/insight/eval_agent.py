"""
Eval Agent (Benchmark Scoring)

Uses LLM to evaluate insight quality by comparing against event.* ground truth.

Spec:
    Input:
        - insight: Dict — from user_behavior_insight table (observation, hypothesis, severity, etc.)
        - events: List[Dict] — event.* records within ±7 days of the insight

    Output:
        EvalResult:
            - match_score: float (0-1) — overall quality score
            - matched_event: Optional[Dict] — best matching event
            - time_relevance: float (0-1) — how close in time
            - semantic_relevance: float (0-1) — how related the insight is to the event
            - detection_quality: float (0-1) — are the right indicators flagged
            - touch_quality: float (0-1) — is the touch_message appropriate (if present)
            - compliance_pass: bool — does touch_message pass compliance
            - feedback: str — textual evaluation feedback
"""

import logging
from typing import Any, Dict, List, Optional


# JSON Schema for eval output
EVAL_RESPONSE_SCHEMA = {
    "type": "json_schema",
    "json_schema": {
        "name": "insight_evaluation",
        "strict": True,
        "schema": {
            "type": "object",
            "properties": {
                "match_score": {
                    "type": "number",
                    "description": "Overall quality score 0-1. 1=perfect detection of a real event, 0=complete false positive"
                },
                "matched_event_name": {
                    "type": "string",
                    "description": "Name of the best matching event, or 'none' if no match"
                },
                "time_relevance": {
                    "type": "number",
                    "description": "0-1, how close the insight date is to the event date"
                },
                "semantic_relevance": {
                    "type": "number",
                    "description": "0-1, how well the insight observation relates to the actual event"
                },
                "detection_quality": {
                    "type": "number",
                    "description": "0-1, are the correct indicators flagged for this type of event"
                },
                "touch_quality": {
                    "type": "number",
                    "description": "0-1, is the touch message appropriate, helpful, and natural"
                },
                "compliance_pass": {
                    "type": "boolean",
                    "description": "True if touch message has no diagnostic conclusions or prescription recommendations"
                },
                "is_true_positive": {
                    "type": "boolean",
                    "description": "True if the insight correctly detected a real event"
                },
                "is_false_positive": {
                    "type": "boolean",
                    "description": "True if the insight has no corresponding real event"
                },
                "feedback": {
                    "type": "string",
                    "description": "Brief textual evaluation in Chinese"
                }
            },
            "required": [
                "match_score", "matched_event_name", "time_relevance",
                "semantic_relevance", "detection_quality", "touch_quality",
                "compliance_pass", "is_true_positive", "is_false_positive", "feedback"
            ],
            "additionalProperties": False
        }
    }
}

SYSTEM_PROMPT = """You are a health insight evaluation expert. You evaluate whether AI-generated health insights correctly detected real health events.

You are given:
1. An insight generated by our system (observation, hypothesis, severity)
2. Real events that happened around the same time (from ground truth)

Your job is to score the insight on multiple dimensions.

Scoring guidelines:
- match_score: 0.8+ if insight clearly corresponds to a real event; 0.5-0.8 if partially related; 0.2-0.5 if tangentially related; <0.2 if no relation
- time_relevance: 1.0 if same day, 0.8 if ±1-2 days, 0.5 if ±3-5 days, 0.2 if ±6-7 days, 0 if no event nearby
- semantic_relevance: Does the insight description match what actually happened? 1.0=exact match, 0=unrelated
- detection_quality: Are the flagged indicators (heartRate, bloodGlucose, etc.) appropriate for this event type?
- touch_quality: Is the user-facing message helpful, natural, and actionable? (0 if no touch_message)
- compliance_pass: Does the touch message avoid medical diagnoses and prescription recommendations?
- is_true_positive: True if match_score >= 0.5
- is_false_positive: True if no events within window OR match_score < 0.3

Respond in Chinese for the feedback field."""

USER_PROMPT_TEMPLATE = """## 洞察（系统产出）
- 日期: {target_date}
- 配方: {recipe_name}
- 严重度: {severity}
- 观察 (Layer 1): {observation}
- 假设 (Layer 2): {hypothesis}
- 触达消息 (Layer 3): {touch_message}
- 指标详情: {indicators_detail}

## 真实事件（标准答案，±7天窗口内）
{events_text}

## 评估任务
对比洞察和真实事件，按维度打分。"""


class EvalAgent:
    """Benchmark evaluation agent.

    Usage:
        agent = EvalAgent()
        result = await agent.evaluate(insight, events)
    """

    async def evaluate(
        self,
        insight: Dict[str, Any],
        events: List[Dict[str, Any]],
    ) -> Optional[Dict[str, Any]]:
        """Evaluate a single insight against nearby events.

        Args:
            insight: Row from user_behavior_insight
            events: List of event.* records within ±7 days

        Returns:
            Evaluation result dict, or None if LLM fails
        """
        prompt = self._build_prompt(insight, events)

        try:
            from mirobody.utils.llm.utils import async_get_structured_output

            result = await async_get_structured_output(
                messages=[
                    {"role": "system", "content": SYSTEM_PROMPT},
                    {"role": "user", "content": prompt},
                ],
                response_format=EVAL_RESPONSE_SCHEMA,
                temperature=0.1,  # low temperature for consistent scoring
                max_tokens=800,
            )

            if result:
                logging.info(
                    f"[EvalAgent] Scored insight {insight.get('id')}: "
                    f"match={result.get('match_score')}, tp={result.get('is_true_positive')}"
                )
            return result

        except Exception as e:
            logging.error(f"[EvalAgent] Failed for insight {insight.get('id')}: {e}")
            return None

    def _build_prompt(
        self,
        insight: Dict[str, Any],
        events: List[Dict[str, Any]],
    ) -> str:
        """Build evaluation prompt."""

        # Format events
        if events:
            event_parts = []
            for e in events:
                event_parts.append(
                    f"- [{e.get('event_date')}] {e.get('event_type')}: {e.get('event_name')}"
                )
            events_text = "\n".join(event_parts)
        else:
            events_text = "无（该时间窗口内无已知事件）"

        # Format indicators detail
        indicators = insight.get("indicators_detail")
        if isinstance(indicators, str):
            import json
            try:
                indicators = json.loads(indicators)
            except Exception:
                pass
        if isinstance(indicators, list):
            ind_parts = [
                f"  {d.get('category')}: {d.get('direction')} {d.get('sigma', '?')}σ"
                for d in indicators
            ]
            indicators_text = "\n".join(ind_parts) if ind_parts else "无"
        else:
            indicators_text = str(indicators) if indicators else "无"

        return USER_PROMPT_TEMPLATE.format(
            target_date=insight.get("target_date", "?"),
            recipe_name=insight.get("recipe_name", "?"),
            severity=insight.get("severity", "?"),
            observation=insight.get("observation", "无"),
            hypothesis=insight.get("hypothesis", "无 (Layer 2 未执行)"),
            touch_message=insight.get("touch_message", "无 (Layer 3 未执行)"),
            indicators_detail=indicators_text,
            events_text=events_text,
        )
